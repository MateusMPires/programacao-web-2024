// Declaração das variáveis numéricas e inicialização
let a = 10;
let b = 5;

// Operações
let soma = a + b;
let subtracao = a - b;
let multiplicacao = a * b;
let divisao = a / b;

// Impressão dos resultados
console.log(`Resultado da soma entre ${a} e ${b}: ${soma}`);
console.log(`Resultado da subtração entre ${a} e ${b}: ${subtracao}`);
console.log(`Resultado da multiplicação entre ${a} e ${b}: ${multiplicacao}`);
console.log(`Resultado da divisão entre ${a} e ${b}: ${divisao}`);